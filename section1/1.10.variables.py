# no spaces in a variable, only underscores
# spaces for readability like inbetween operators
items = 10
price = 2

total_price = items * price
print(total_price)

# Tip
# Tip 1 - Printing Multiple Variables 

# It is also possible to print out multiple variables in one single print function by using commas between the variables. 
# The last line of the code below contains an example:

items = 10
price = 2
 
total_price = items * price
 
# Below we print out three variables
print(total_price, items, price) 
# Output: 20 10 2